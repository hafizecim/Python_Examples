#*********** KARŞILAŞTIRMA OPERATÖRÜ ************
# 🆚 Karşılaştırma Operatörleri: <, >, <=, >=, ==, !=
a = 20  # a'ya 20 atanıyor
b = 5   # b'ye 5 atanıyor

print(a > b)  # a, b'den büyük mü? 🏆 True çünkü 20 > 5
print(a == b) # a, b'ye eşit mi? 🤔 False çünkü 20 != 5
print(a < b)  # a, b'den küçük mü? ⬇️ False çünkü 20 > 5
print(a >= b) # a, b'ye eşit veya büyük mü? 📈 True çünkü 20 >= 5
print(a != b) # a, b'ye eşit değil mi? ❌ True çünkü 20 != 5
